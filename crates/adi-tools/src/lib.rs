        std::fs::create_dir_all(&dir).expect("mkdir");
        let target = dir.join("build.ts");
        std::fs::write(&target, "console.log('x')\n").expect("write target");

        let tool = store
            .link(&target.to_string_lossy(), None, None, None, None)
            .expect("link");
